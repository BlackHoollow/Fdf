int		define_color(int **tab, int i, int j)
{
	if (tab[i][j] <= 0)
		return (0x00FFFFFF);
	if (tab[i][j] > 0 && tab[i][j] <= 5)
		return (0x000000FF);
	if (tab[i][j] > 5 && tab[i][j] <= 10)
		return (0x00FF0000);
	return (0x00FFFF00);
}
